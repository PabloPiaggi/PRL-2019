#!/bin/bash -l

############################################################################
# This is an example of how to run the simulation
############################################################################

############################################################################
EXE=mdrun
Project=Water
############################################################################


############################################################################
# Run first time
############################################################################
mpirun -np 4 ${EXE} -plumed plumed.start.dat -s topol.tpr -deffnm ${Project} &> ${Project}.out

############################################################################
# Run job continuation
############################################################################
mpirun -np 4 ${EXE} -plumed plumed.dat -s topol.tpr -cpi ${Project}.cpt -deffnm ${Project} &> ${Project}.out
